package Lab5.ex2.calculators;

public class BasicCalculator {
    public final double add(double n1, double n2) {
        return n1 + n2;
    }

    public final double subtract(double n1, double n2) {
        return n1 - n2;
    }

    public final double multiply(double n1, double n2) {
        return n1 * n2;
    }

    public final double divide(double n1, double n2) {
        return n1 / n2;
    }
}
